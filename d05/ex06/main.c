#include <stdio.h>
#include <string.h>

int	ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		return (s1[i] - s2[i]);
		i++;
	}
	if ((s1[i] == '\0' && s2[i] != '\0') || (s2[i] == '\0' && s1[i] != '\0'))
		return (s1[i] - s2[i]);
	return (0);
}

int	main(void)
 {
   char str2[10] = "asd0";
   char str1[10] = "asd";
   int result1 = strcmp(str1, str2);
   int result2 = ft_strcmp(str1, str2);
   printf("%i %i", result1, result2);
  return (0);
 }
